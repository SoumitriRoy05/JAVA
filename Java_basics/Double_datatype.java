package Java_basics;

public class Double_datatype {
    public static void main(String[] args) {
        double x=5;
        double y=2;
        System.out.println(x/y);
        System.out.println(5/2);
        System.out.println(x+y);
        System.out.println(x-y);
        System.out.println(x*y);
    }
}
